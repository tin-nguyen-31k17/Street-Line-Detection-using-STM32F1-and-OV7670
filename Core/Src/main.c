void send_string(const char* str) {
  for (size_t i = 0; i < strlen(str); i++) {
    HAL_UART_Transmit(&huart2, (uint8_t*)&str[i], 1, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(RED_LED_GPIO_Port, RED_LED_Pin, 1);
  }
}
